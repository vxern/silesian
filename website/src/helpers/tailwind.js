export function resolveTailwindColour(colour) {
  switch (colour) {
    case "red":
      return "bg-red-400";
    case "orange":
      return "bg-orange-400";
    case "amber":
      return "bg-amber-400";
    case "yellow":
      return "bg-yellow-400";
    case "lime":
      return "bg-lime-400";
    case "green":
      return "bg-green-400";
    case "emerald":
      return "bg-emerald-400";
    case "teal":
      return "bg-teal-400";
    case "cyan":
      return "bg-cyan-400";
    case "sky":
      return "bg-sky-400";
    case "blue":
      return "bg-blue-400";
    case "indigo":
      return "bg-indigo-400";
    case "violet":
      return "bg-violet-400";
    case "purple":
      return "bg-purple-400";
    case "fuchsia":
      return "bg-fuchsia-400";
    case "pink":
      return "bg-pink-400";
    case "rose":
      return "bg-rose-400";
    case "slate":
      return "bg-slate-400";
    case "gray":
      return "bg-gray-400";
    case "zinc":
      return "bg-zinc-400";
    case "neutral":
      return "bg-neutral-400";
    case "stone":
      return "bg-stone-400";
  }
}